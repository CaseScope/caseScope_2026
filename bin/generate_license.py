#!/usr/bin/env python3
"""Generate a signed license file for CaseScope.

This script runs on your ACTIVATION SERVER to generate licenses.
It requires the private key generated by generate_license_keypair.py.

Usage:
    python generate_license.py --customer-id acme --customer-name "ACME Corp" \
        --fingerprint <fingerprint_json_file> \
        --features ai,opencti \
        --expires 2027-01-30

Example fingerprint JSON (from client's activation request):
{
    "fingerprint": {
        "fingerprint_hash": "abc123...",
        "components": {
            "machine_uuid": "hash...",
            "disk_serial": "hash...",
            ...
        }
    }
}
"""

import argparse
import base64
import json
import os
import sys
from datetime import datetime, timedelta

try:
    from cryptography.hazmat.primitives.asymmetric.ed25519 import Ed25519PrivateKey
    from cryptography.hazmat.primitives import serialization
except ImportError:
    print("Error: cryptography package not installed.")
    print("Install with: pip install cryptography>=42.0.0")
    sys.exit(1)


def load_private_key(key_file: str) -> Ed25519PrivateKey:
    """Load private key from PEM file."""
    with open(key_file, 'rb') as f:
        return serialization.load_pem_private_key(f.read(), password=None)


def generate_license(
    private_key: Ed25519PrivateKey,
    license_id: str,
    customer_id: str,
    customer_name: str,
    fingerprint_components: dict,
    features: dict,
    expires_at: datetime,
    issued_at: datetime = None
) -> dict:
    """Generate a signed license."""
    
    if issued_at is None:
        issued_at = datetime.utcnow()
    
    # Build license data (everything except signature)
    license_data = {
        'license_id': license_id,
        'customer_id': customer_id,
        'customer_name': customer_name,
        'issued_at': issued_at.strftime('%Y-%m-%dT%H:%M:%SZ'),
        'expires_at': expires_at.strftime('%Y-%m-%dT%H:%M:%SZ'),
        'features': features,
        'fingerprint_components': fingerprint_components
    }
    
    # Serialize for signing (sorted keys, compact)
    message = json.dumps(license_data, sort_keys=True, separators=(',', ':')).encode()
    
    # Sign
    signature = private_key.sign(message)
    signature_b64 = base64.b64encode(signature).decode()
    
    # Add signature to license
    license_data['signature'] = signature_b64
    
    return license_data


def main():
    parser = argparse.ArgumentParser(description='Generate a signed CaseScope license')
    parser.add_argument('--private-key', default='license_private_key.pem',
                       help='Path to private key PEM file')
    parser.add_argument('--license-id', required=True,
                       help='License ID (e.g., CS-2026-0001)')
    parser.add_argument('--customer-id', required=True,
                       help='Customer ID')
    parser.add_argument('--customer-name', required=True,
                       help='Customer name')
    parser.add_argument('--fingerprint', required=True,
                       help='Path to fingerprint JSON (from activation request)')
    parser.add_argument('--features', default='ai,opencti',
                       help='Comma-separated features to enable (ai,opencti)')
    parser.add_argument('--expires', required=True,
                       help='Expiry date (YYYY-MM-DD)')
    parser.add_argument('--output', default=None,
                       help='Output file (default: license_<customer_id>.json)')
    
    args = parser.parse_args()
    
    # Load private key
    try:
        private_key = load_private_key(args.private_key)
        print(f"[+] Loaded private key from {args.private_key}")
    except FileNotFoundError:
        print(f"Error: Private key not found: {args.private_key}")
        print("Run generate_license_keypair.py first")
        sys.exit(1)
    
    # Load fingerprint
    try:
        with open(args.fingerprint, 'r') as f:
            fingerprint_data = json.load(f)
        
        # Handle both activation request format and direct component format
        if 'fingerprint' in fingerprint_data:
            components = fingerprint_data['fingerprint'].get('components', {})
        elif 'components' in fingerprint_data:
            components = fingerprint_data['components']
        else:
            components = fingerprint_data
        
        print(f"[+] Loaded fingerprint with {len(components)} components")
    except FileNotFoundError:
        print(f"Error: Fingerprint file not found: {args.fingerprint}")
        sys.exit(1)
    except json.JSONDecodeError as e:
        print(f"Error: Invalid JSON in fingerprint file: {e}")
        sys.exit(1)
    
    # Parse features
    feature_list = [f.strip() for f in args.features.split(',')]
    features = {
        'ai': 'ai' in feature_list,
        'opencti': 'opencti' in feature_list,
        'max_cases': -1  # Unlimited
    }
    
    # Parse expiry date
    try:
        expires_at = datetime.strptime(args.expires, '%Y-%m-%d')
    except ValueError:
        print(f"Error: Invalid date format: {args.expires} (use YYYY-MM-DD)")
        sys.exit(1)
    
    # Generate license
    license_data = generate_license(
        private_key=private_key,
        license_id=args.license_id,
        customer_id=args.customer_id,
        customer_name=args.customer_name,
        fingerprint_components=components,
        features=features,
        expires_at=expires_at
    )
    
    # Save license
    output_file = args.output or f"license_{args.customer_id}.json"
    with open(output_file, 'w') as f:
        json.dump(license_data, f, indent=2)
    
    print(f"[+] License generated: {output_file}")
    print()
    print("License details:")
    print(f"  License ID:   {license_data['license_id']}")
    print(f"  Customer:     {license_data['customer_name']}")
    print(f"  Features:     {', '.join(k for k, v in features.items() if v is True)}")
    print(f"  Issued:       {license_data['issued_at']}")
    print(f"  Expires:      {license_data['expires_at']}")
    print()
    print("Provide this license file to the customer to activate their CaseScope instance.")


if __name__ == '__main__':
    main()
